"""
Observer: verify multi-block scan kernel correctness via CuPy NVRTC.

Tests the three-phase protocol that engine.rs::generate_multiblock_scan produces:
1. scan_per_block — per-block inclusive scan + block totals
2. scan_block_totals — single-block scan of totals
3. propagate_extract — add block prefixes + extract output

This is the EXACT kernel source that the Rust engine generates for AddOp,
reconstructed from engine.rs (since the crate can't compile yet).
"""
import cupy as cp
import numpy as np
import time

# Reconstructed from engine.rs::generate_multiblock_scan(&AddOp)
MULTIBLOCK_ADD_KERNEL = r"""
// Multi-block parallel scan for operator: add
// Three-phase: scan_per_block -> scan_block_totals -> propagate_extract
// Generated by winrapids-scan (Rust)

typedef double state_t;

__device__ __forceinline__ state_t make_identity() {
    state_t id = 0.0;
    return id;
}

__device__ __forceinline__ state_t lift_element(double x) {
    state_t s = x;
    return s;
}

__device__ __forceinline__ state_t combine_states(state_t a, state_t b) {
    state_t result = (a + b);
    return result;
}

__device__ __forceinline__ double extract_primary(state_t s) {
    return s;
}

// Phase 1: Per-block inclusive scan.
extern "C" __global__ void scan_per_block(
    const double* __restrict__ input,
    state_t* __restrict__ state_out,
    state_t* __restrict__ block_totals,
    int n
) {
    extern __shared__ char shared_raw[];
    state_t* shared = (state_t*)shared_raw;

    int tid = threadIdx.x;
    int gid = blockIdx.x * blockDim.x + tid;

    shared[tid] = (gid < n) ? lift_element(input[gid]) : make_identity();
    __syncthreads();

    // Up-sweep (reduce)
    for (int stride = 1; stride < blockDim.x; stride *= 2) {
        int idx = (tid + 1) * stride * 2 - 1;
        if (idx < blockDim.x) {
            shared[idx] = combine_states(shared[idx - stride], shared[idx]);
        }
        __syncthreads();
    }

    // Down-sweep (distribute)
    for (int stride = blockDim.x / 4; stride > 0; stride /= 2) {
        int idx = (tid + 1) * stride * 2 - 1;
        if (idx + stride < blockDim.x) {
            shared[idx + stride] = combine_states(shared[idx], shared[idx + stride]);
        }
        __syncthreads();
    }

    if (gid < n) state_out[gid] = shared[tid];
    // Last thread in block writes the block total
    if (tid == blockDim.x - 1) block_totals[blockIdx.x] = shared[tid];
}

// Phase 2: Single-block scan of block totals.
extern "C" __global__ void scan_block_totals(
    state_t* __restrict__ totals,
    int n_blocks
) {
    extern __shared__ char shared_raw[];
    state_t* shared = (state_t*)shared_raw;

    int tid = threadIdx.x;
    shared[tid] = (tid < n_blocks) ? totals[tid] : make_identity();
    __syncthreads();

    for (int stride = 1; stride < blockDim.x; stride *= 2) {
        int idx = (tid + 1) * stride * 2 - 1;
        if (idx < blockDim.x) {
            shared[idx] = combine_states(shared[idx - stride], shared[idx]);
        }
        __syncthreads();
    }

    for (int stride = blockDim.x / 4; stride > 0; stride /= 2) {
        int idx = (tid + 1) * stride * 2 - 1;
        if (idx + stride < blockDim.x) {
            shared[idx + stride] = combine_states(shared[idx], shared[idx + stride]);
        }
        __syncthreads();
    }

    if (tid < n_blocks) totals[tid] = shared[tid];
}

// Phase 3: Propagate block prefixes and extract output(s).
extern "C" __global__ void propagate_extract(
    double* __restrict__ out0,
    double* __restrict__ out1,
    const state_t* __restrict__ state_in,
    const state_t* __restrict__ block_prefixes,
    int n
) {
    int gid = blockIdx.x * blockDim.x + threadIdx.x;
    if (gid < n) {
        state_t s = state_in[gid];
        if (blockIdx.x > 0) {
            s = combine_states(block_prefixes[blockIdx.x - 1], s);
        }
        out0[gid] = extract_primary(s);
    }
}
"""

BLOCK_SIZE = 1024

def run_multiblock_scan(module, data, n):
    """Execute three-phase multi-block scan on GPU."""
    n_blocks = (n + BLOCK_SIZE - 1) // BLOCK_SIZE
    shared_bytes = BLOCK_SIZE * 8  # sizeof(double)

    d_input = cp.asarray(data)
    d_state = cp.zeros(n, dtype=cp.float64)
    d_totals = cp.zeros(n_blocks, dtype=cp.float64)
    d_out0 = cp.zeros(n, dtype=cp.float64)
    d_out1 = cp.zeros(1, dtype=cp.float64)  # dummy for single-output

    scan_per_block = module.get_function('scan_per_block')
    scan_block_totals = module.get_function('scan_block_totals')
    propagate_extract = module.get_function('propagate_extract')

    # Phase 1
    scan_per_block((n_blocks,), (BLOCK_SIZE,),
                   (d_input, d_state, d_totals, n),
                   shared_mem=shared_bytes)

    # Phase 2 (only for multi-block)
    if n_blocks > 1:
        totals_block_size = 1
        while totals_block_size < n_blocks:
            totals_block_size *= 2
        scan_block_totals((1,), (totals_block_size,),
                         (d_totals, n_blocks),
                         shared_mem=totals_block_size * 8)

    # Phase 3
    propagate_extract((n_blocks,), (BLOCK_SIZE,),
                     (d_out0, d_out1, d_state, d_totals, n))

    cp.cuda.Stream.null.synchronize()
    return cp.asnumpy(d_out0)


def main():
    print("=" * 70)
    print("Observer: Multi-block Scan Kernel Verification")
    print("=" * 70)

    # Compile
    print("\n-- NVRTC Compilation --\n")
    t0 = time.perf_counter()
    module = cp.RawModule(code=MULTIBLOCK_ADD_KERNEL, options=('--std=c++17',))
    t1 = time.perf_counter()
    print(f"  Compile + load: {(t1-t0)*1000:.1f} ms  PASS")

    # Correctness across sizes
    print("\n-- Correctness: Multi-block Cumsum --\n")

    for n in [5, 256, 1024, 1025, 2048, 10_000, 50_000, 100_000, 500_000, 900_000]:
        rng = np.random.default_rng(42)
        data = rng.standard_normal(n).astype(np.float64)

        result = run_multiblock_scan(module, data, n)
        reference = np.cumsum(data)
        max_err = np.max(np.abs(result - reference))

        n_blocks = (n + BLOCK_SIZE - 1) // BLOCK_SIZE
        status = "PASS" if max_err < 1e-6 else "FAIL"
        print(f"  n={n:>9,} ({n_blocks:>3} blocks): max_err={max_err:.2e}  {status}")

    # Performance benchmark
    print("\n-- Performance: Three-phase scan vs CuPy cumsum --\n")

    for n in [50_000, 100_000, 500_000, 900_000]:
        d = cp.random.randn(n).astype(cp.float64)
        data_np = cp.asnumpy(d)
        n_blocks = (n + BLOCK_SIZE - 1) // BLOCK_SIZE

        # CuPy cumsum baseline
        for _ in range(3):
            cp.cumsum(d)
        cp.cuda.Stream.null.synchronize()

        times_cupy = []
        for _ in range(20):
            cp.cuda.Stream.null.synchronize()
            t0 = time.perf_counter_ns()
            cp.cumsum(d)
            cp.cuda.Stream.null.synchronize()
            t1 = time.perf_counter_ns()
            times_cupy.append((t1 - t0) / 1000)
        times_cupy.sort()
        p50_cupy = times_cupy[10]

        # Multi-block scan
        for _ in range(3):
            run_multiblock_scan(module, data_np, n)

        times_mb = []
        for _ in range(20):
            cp.cuda.Stream.null.synchronize()
            t0 = time.perf_counter_ns()
            run_multiblock_scan(module, data_np, n)
            t1 = time.perf_counter_ns()
            times_mb.append((t1 - t0) / 1000)
        times_mb.sort()
        p50_mb = times_mb[10]

        ratio = p50_mb / p50_cupy
        print(f"  n={n:>9,} ({n_blocks:>3} blk): cupy={p50_cupy:7.1f} us  "
              f"multiblock={p50_mb:7.1f} us  ratio={ratio:.2f}x")

    print(f"\n{'=' * 70}")
    print("Multi-block verification complete.")
    print("=" * 70)


if __name__ == "__main__":
    main()
