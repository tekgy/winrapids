mod multi_adic_parity {
    use super::*;
    use tambear::multi_adic::*;

    // ── p-adic valuation ────────────────────────────────────────────────

    #[test]
    fn valuation_known_values() {
        assert_eq!(valuation(12, 2), 2);   // 12 = 2^2 * 3
        assert_eq!(valuation(12, 3), 1);
        assert_eq!(valuation(12, 5), 0);
        assert_eq!(valuation(8, 2), 3);    // 8 = 2^3
        assert_eq!(valuation(1, 2), 0);
        assert_eq!(valuation(0, 2), u32::MAX);
        assert_eq!(valuation(32, 2), 5);   // 2^5
        assert_eq!(valuation(243, 3), 5);  // 3^5
        assert_eq!(valuation(3125, 5), 5); // 5^5
        assert_eq!(valuation(2520, 2), 3); // 2520 = 2^3 * 3^2 * 5 * 7
        assert_eq!(valuation(2520, 3), 2);
        assert_eq!(valuation(2520, 5), 1);
        assert_eq!(valuation(2520, 7), 1);
    }

    // ── p-adic norm ─────────────────────────────────────────────────────

    #[test]
    fn norm_known_values() {
        assert_close("|8|_2", p_adic_norm(8, 2), 0.125, 1e-15);
        assert_close("|9|_3", p_adic_norm(9, 3), 1.0 / 9.0, 1e-15);
        assert_close("|7|_2", p_adic_norm(7, 2), 1.0, 1e-15);
        assert_close("|0|_2", p_adic_norm(0, 2), 0.0, 1e-15);
        assert_close("|25|_5", p_adic_norm(25, 5), 0.04, 1e-15);
    }

    // ── p-adic distance ─────────────────────────────────────────────────

    #[test]
    fn distance_known_values() {
        assert_close("d_2(0,8)", p_adic_distance(0, 8, 2), 0.125, 1e-15);
        assert_close("d_2(0,4)", p_adic_distance(0, 4, 2), 0.25, 1e-15);
        assert_close("d_2(7,7)", p_adic_distance(7, 7, 2), 0.0, 1e-15);
        assert_close("d_2(6,12)", p_adic_distance(6, 12, 2), 0.5, 1e-15);
    }

    // ── Ultrametric inequality ──────────────────────────────────────────

    #[test]
    fn ultrametric_inequality_holds() {
        for &p in &[2u64, 3, 5] {
            for &(a, b, c) in &[(1u64, 3, 5), (2, 6, 10), (7, 15, 31)] {
                let dac = p_adic_distance(a, c, p);
                let dab = p_adic_distance(a, b, p);
                let dbc = p_adic_distance(b, c, p);
                assert!(
                    dac <= dab.max(dbc) + 1e-15,
                    "Ultrametric failed p={}: d({},{})={} > max(d({},{})={}, d({},{})={})",
                    p, a, c, dac, a, b, dab, b, c, dbc
                );
            }
        }
    }

    // ── p-adic digits roundtrip ─────────────────────────────────────────

    #[test]
    fn digits_roundtrip() {
        for &p in &[2u64, 3, 5, 7, 10] {
            for &n in &[0u64, 1, 42, 100, 12345, 999999] {
                let digits = p_adic_digits(n, p, 64);
                let reconstructed = from_p_adic_digits(&digits, p);
                assert_eq!(reconstructed, n, "roundtrip failed n={}, p={}", n, p);
            }
        }
    }

    // ── Known digit expansions ──────────────────────────────────────────

    #[test]
    fn known_digit_expansions() {
        // 13 in binary: 1101 → [1, 0, 1, 1] (low-order first)
        assert_eq!(p_adic_digits(13, 2, 10), vec![1, 0, 1, 1]);
        // 23 in base 3: [2, 1, 2]
        assert_eq!(p_adic_digits(23, 3, 10), vec![2, 1, 2]);
    }

    // ── Multi-adic profile ──────────────────────────────────────────────

    #[test]
    fn profile_known_numbers() {
        // 60 = 2^2 * 3 * 5
        let p60 = MultiAdicProfile::default_of(60);
        assert_eq!(p60.v(2), Some(2));
        assert_eq!(p60.v(3), Some(1));
        assert_eq!(p60.v(5), Some(1));
        assert_eq!(p60.v(7), Some(0));

        // 11 is coprime to {2,3,5,7}
        let p11 = MultiAdicProfile::default_of(11);
        assert!(p11.is_coprime_to_all());

        // 2520 = 2^3 * 3^2 * 5 * 7
        let p2520 = MultiAdicProfile::default_of(2520);
        assert_eq!(p2520.total_weight(), 7);
    }

    // ── Batch profiles ──────────────────────────────────────────────────

    #[test]
    fn batch_profiles_10_5() {
        let primes = [2u64, 3, 5];
        let result = batch_profiles(10, 5, &primes);
        // n=10=2*5: v2=1, v3=0, v5=1
        assert_eq!(result[0], 1);
        assert_eq!(result[1], 0);
        assert_eq!(result[2], 1);
        // n=12=2^2*3: v2=2, v3=1, v5=0
        assert_eq!(result[6], 2);
        assert_eq!(result[7], 1);
        assert_eq!(result[8], 0);
    }
}

