mod series_accel_parity {
    use super::*;
    use tambear::series_accel::*;

    const PI_OVER_4: f64 = std::f64::consts::FRAC_PI_4;
    const PI_SQ_OVER_6: f64 = 1.6449340668482264;
    const LN_2: f64 = std::f64::consts::LN_2;

    // ── Partial sums ────────────────────────────────────────────────────

    #[test]
    fn partial_sums_basic() {
        let terms = vec![1.0, 2.0, 3.0, 4.0];
        let sums = partial_sums(&terms);
        assert_slices_close("partial_sums", &sums, &[1.0, 3.0, 6.0, 10.0], 1e-15);
    }

    // ── Cesaro: Grandi's series → 1/2 ───────────────────────────────────

    #[test]
    fn cesaro_grandi_converges_to_half() {
        // 1 - 1 + 1 - 1 + ... Cesaro sum should approach 0.5
        let terms: Vec<f64> = (0..100).map(|n| if n % 2 == 0 { 1.0 } else { -1.0 }).collect();
        let sums = partial_sums(&terms);
        let cs = cesaro_sum(&sums);
        assert_close("cesaro_grandi", cs, 0.5, 0.01);
    }

    // ── Aitken on Leibniz ───────────────────────────────────────────────

    #[test]
    fn aitken_leibniz_improves_convergence() {
        let n = 20;
        let terms: Vec<f64> = (0..n).map(|k| {
            (-1.0f64).powi(k as i32) / (2 * k + 1) as f64
        }).collect();
        let sums = partial_sums(&terms);
        let raw_error = (sums[n - 1] - PI_OVER_4).abs();
        let accel = aitken_delta2(&sums);
        let accel_error = (accel.last().unwrap() - PI_OVER_4).abs();
        // Aitken should reduce error by at least 100x
        assert!(
            accel_error < raw_error / 10.0,
            "Aitken should improve: raw_err={:.2e}, accel_err={:.2e}",
            raw_error, accel_error
        );
    }

    // ── Wynn epsilon on Leibniz ─────────────────────────────────────────

    #[test]
    fn wynn_leibniz_converges() {
        let n = 20;
        let terms: Vec<f64> = (0..n).map(|k| {
            (-1.0f64).powi(k as i32) / (2 * k + 1) as f64
        }).collect();
        let sums = partial_sums(&terms);
        let estimate = wynn_epsilon(&sums);
        assert_close("wynn_leibniz", estimate, PI_OVER_4, 1e-6);
    }

    // ── Wynn epsilon on ln(2) ───────────────────────────────────────────

    #[test]
    fn wynn_ln2_converges() {
        let n = 20;
        let terms: Vec<f64> = (0..n).map(|k| {
            (-1.0f64).powi(k as i32) / (k + 1) as f64
        }).collect();
        let sums = partial_sums(&terms);
        let estimate = wynn_epsilon(&sums);
        assert_close("wynn_ln2", estimate, LN_2, 1e-6);
    }

    // ── Richardson extrapolation on Basel ────────────────────────────────

    #[test]
    fn richardson_basel() {
        // Basel series partial sums at N=5,10,20,40 with ratio=2, order=1
        let approxs: Vec<f64> = [5, 10, 20, 40].iter().map(|&n| {
            (1..=n).map(|k| 1.0 / (k * k) as f64).sum::<f64>()
        }).collect();
        let estimate = richardson_extrapolate(&approxs, 2.0, 1);
        // Python oracle: error ≈ 3e-7
        assert_close("richardson_basel", estimate, PI_SQ_OVER_6, 1e-5);
    }

    // ── Euler transform on ln(2) ────────────────────────────────────────

    #[test]
    fn euler_transform_ln2() {
        let n = 20;
        let terms: Vec<f64> = (0..n).map(|k| {
            (-1.0f64).powi(k as i32) / (k + 1) as f64
        }).collect();
        let estimate = euler_transform(&terms);
        // Euler transform should be close to ln(2)
        assert_close("euler_ln2", estimate, LN_2, 1e-3);
    }

    // ── Streaming Wynn ──────────────────────────────────────────────────

    #[test]
    fn streaming_wynn_leibniz() {
        let mut sw = StreamingWynn::new(40);
        for k in 0..30 {
            let term = (-1.0f64).powi(k) / (2 * k + 1) as f64;
            sw.push(term);
        }
        assert_close("streaming_wynn_leibniz", sw.estimate(), PI_OVER_4, 1e-6);
    }
}

