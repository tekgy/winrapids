    // ── FFT: DC signal → all energy at bin 0 ────────────────────────────

    #[test]
    fn fft_dc_8pt() {
        // numpy.fft.fft([1,1,1,1,1,1,1,1]) = [8, 0, 0, 0, 0, 0, 0, 0]
        let input: Vec<(f64, f64)> = vec![(1.0, 0.0); 8];
        let result = fft(&input);
        assert_close("fft_dc[0].re", result[0].0, 8.0, 1e-12);
        for i in 1..8 {
            assert_close(&format!("fft_dc[{}].re", i), result[i].0, 0.0, 1e-12);
            assert_close(&format!("fft_dc[{}].im", i), result[i].1, 0.0, 1e-12);
        }
    }

    // ── FFT: impulse → flat spectrum ────────────────────────────────────

