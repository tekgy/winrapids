use tambear::special_functions::normal_cdf;

#[test]
fn ncdf_oracle_full() {
    fn ulps(a: f64, b: f64) -> i64 {
        if a == b { return 0; }
        let ai = a.to_bits() as i64;
        let bi = b.to_bits() as i64;
        (ai - bi).abs()
    }
    let cases: &[(f64, f64)] = &[
        (0.0, 0.5),
        (0.1, 0.539827837277029),
        (0.5, 0.6914624612740131),
        (1.0, 0.8413447460685429),
        (1.5, 0.9331927987311419),
        (1.96, 0.9750021048517795),
        (2.0, 0.9772498680518208),
        (2.576, 0.995002467684265),
        (3.0, 0.9986501019683699),
        (3.5, 0.9997673709209645),
        (4.0, 0.9999683287581669),
        (5.0, 0.9999997133484281),
        (6.0, 0.9999999990134123),
        (7.0, 0.9999999999987201),
        (-0.1, 0.460172162722971),
        (-0.5, 0.3085375387259869),
        (-1.0, 0.15865525393145705),
        (-1.96, 0.024997895148220435),
        (-2.0, 0.02275013194817921),
        (-3.0, 0.0013498980316300946),
        (-4.0, 3.1671241833119924e-05),
        (-5.0, 2.866515718791939e-07),
        (-6.0, 9.86587645037698e-10),
        (-7.0, 1.279812543885835e-12),
    ];
    let mut max_ulps = 0i64;
    for &(x, oracle) in cases {
        let got = normal_cdf(x);
        let u = ulps(got, oracle);
        println!("Phi({:6.3}) got={:.17e} oracle={:.17e} ULPs={}", x, got, oracle, u);
        max_ulps = max_ulps.max(u);
    }
    println!("max ULPs = {}", max_ulps);
}
